/**
 * den metoden beräknar fakultet dvs n!, det är lite komplicerat beräkning och har jag använd extra metod som heter multiplicera
 * multiplicera metod beräknar fakultet
 * jag har använd long som typ av värde för att man kunna beräkna största talen
 * i den här metoden vill jag hantera två olika fel exception som kan ske
 * @param evt 
 */
    private void btnFakultetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFakultetActionPerformed
        if(txtDisplay.getText().equals("")){
            txtDisplay.setText("0");
        }
          try{
         long tal = Long.parseLong(txtDisplay.getText());
         if (tal < 0){
           txtDisplay.setText("Error"); 
           return;
         }
          
         long fakultet = 1;
        for (long i = 1; i <= tal; i++){
            fakultet = fakultet * i;
        }
          txtDisplay.setText(Long.toString(fakultet));
          }
          
          catch (NumberFormatException e){
            txtDisplay.setText("Error");  
          }
        
      
    }//GEN-LAST:event_btnFakultetActionPerformed

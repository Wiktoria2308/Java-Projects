/**
 * när man klickar på +/- knapp så talen ska ändra sig till negativ talen eller till positiv talen 
 * @param evt 
 */
    private void btnPlusMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlusMinusActionPerformed
           if(txtDisplay.getText().equals("")){
            txtDisplay.setText("0");
            
        }  
        double tal = Double.parseDouble(txtDisplay.getText()); // först tilldelar värde från displayen till double variabel
            
            if (tal > 0){ // om talen i displayen är större än noll så ska det ändra sig till negativ talen
            String omvandlaTillminus = "-" + txtDisplay.getText();
            txtDisplay.setText(omvandlaTillminus);
            
            }
            else if(tal == 0){
                txtDisplay.setText("0");
            }
            else {
               // om talen på displayen är mindre än 0 så ska det ändra sig till positiv talen
               //genom substring metoden som läser string fråm andra position dvs utan - tecken
            String omvandlaTillplus = txtDisplay.getText();
            String plus = omvandlaTillplus.substring(1);
            txtDisplay.setText(plus); 
            
            }
            
    }//GEN-LAST:event_btnPlusMinusActionPerformed

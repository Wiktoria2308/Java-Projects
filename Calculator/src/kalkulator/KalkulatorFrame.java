/**
 * den metod beräknar olika uttrycker till exempel summa, produkt, kvot
 * först läser det vilket math operator användaren har valt innan och sen beräknar det talen som var lagrat i variabel total1 med talen som finns nu på displayen
 * det också hanterar olika fel som kan ske
 * @param evt 
 */
    private void btnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsActionPerformed
         
        switch ( math_operator ) {
        case '+':
             
           
            total2 = total1 + Double.parseDouble(txtDisplay.getText());
            
          
            if (total2 - Math.floor(total2) == 0 ){ //The Math.floor() function returns the largest integer less than or equal to a given number.
             int produkt = (int)total2; // omvandla produkt till int
            txtDisplay.setText(Integer.toString(produkt)); // skriver ut produkten med int i text field
         }
         else {
             txtDisplay.setText(Double.toString(total2)); // skriver ut produkten med double till text field
         }
            
            break;
            
        case '-':
            
            total2 = total1 - Double.parseDouble(txtDisplay.getText());
           
            if(total2 - Math.floor(total2) == 0) {
                int produkt = (int)total2;
                txtDisplay.setText(Integer.toString(produkt));
            }
            else {
             txtDisplay.setText(Double.toString(total2));   
            }
            break;
        case '/':
          
            total2 = total1 / Double.parseDouble(txtDisplay.getText());
            
             if(total2 - Math.floor(total2) == 0) {
                int produkt = (int)total2;
                txtDisplay.setText(Integer.toString(produkt));
            }
            else {
             txtDisplay.setText(Double.toString(total2));   
            }
            break;
        case '*':
           
            total2 = total1 * Double.parseDouble(txtDisplay.getText());
            
             if(total2 - Math.floor(total2) == 0) {
                int produkt = (int)total2;
                txtDisplay.setText(Integer.toString(produkt));
            }
            else {
             txtDisplay.setText(Double.toString(total2));   
            }
            break;

        
        case 'x': // jag har använd den här case för att beräkna x^y, jag gjorde det för att jag behövde lagra första tal för x och
                    //och sen måste jag beräkna det med nästa talen som användaren ska skriva
                    // jag har använd Math.power för att beräkna den här uttryck
         
            total2 = Math.pow(total1, Double.parseDouble(txtDisplay.getText()));
            
             if(total2 - Math.floor(total2) == 0) {
                int produkt = (int)total2;
                txtDisplay.setText(Integer.toString(produkt));
            }
            else {
             txtDisplay.setText(Double.toString(total2));   
            }
            break;
        
        }
       
        total1 = 0; // efter varje beräkning total1 är 0 igen för att man kunna räkna vidare andra uttrycker
        if(txtDisplay.getText().equals("")){
            txtDisplay.setText("0");
            
        }
    }//GEN-LAST:event_btnEqualsActionPerformed
